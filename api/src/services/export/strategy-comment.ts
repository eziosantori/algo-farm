// ---------------------------------------------------------------------------
// Rule translation
// ---------------------------------------------------------------------------

function ruleText(rule: RuleDef): string {
  const lhs = rule.indicator;
  const rhs = rule.compare_to != null ? rule.compare_to : String(rule.value ?? 0);

  switch (rule.condition) {
    case ">":           return `${lhs} > ${rhs}`;
    case "<":           return `${lhs} < ${rhs}`;
    case ">=":          return `${lhs} >= ${rhs}`;
    case "<=":          return `${lhs} <= ${rhs}`;
    case "crosses_above": return `${lhs} crosses above ${rhs}`;
    case "crosses_below": return `${lhs} crosses below ${rhs}`;
    default:            return `${lhs} ${rule.condition} ${rhs}`;
  }
}

