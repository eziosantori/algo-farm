/**
 * Generates a human-readable header comment describing the strategy's
 * working principles (indicators, entry/exit logic, risk management).
 *
 * For 'cs': returns a C# XML /// <summary> block (without trailing newline).
 * For 'pine': returns a line-comment block starting with // === (without trailing newline).
 */
export function generateStrategyPrinciples(
  strategy: StrategyDefinition,
  format: CommentFormat,
): string {
  const date = new Date().toISOString().slice(0, 10);

  const body: string[] = [];
  body.push(`Strategy: ${strategy.name} (${strategy.variant}) — Generated by AlgoFarm ${date}`);
  body.push("");

  // Indicators
  body.push("INDICATORS");
  for (const ind of strategy.indicators) {
    const label = IND_LABELS[ind.type as IndicatorType] ?? ind.type;
    const paramStr = Object.entries(ind.params)
      .filter(([, v]) => typeof v === "number" || typeof v === "string")
      .map(([k, v]) => `${k}=${v}`)
      .join(", ");
    body.push(`  - ${label} (${ind.name})${paramStr ? `: ${paramStr}` : ""}`);
  }
  body.push("");

  // Entry long
  if (strategy.entry_rules.length > 0) {
    body.push("ENTRY LOGIC (LONG)");
    for (const r of strategy.entry_rules) body.push(`  - ${ruleText(r)}`);
    body.push("");
  }

  // Exit long
  if (strategy.exit_rules.length > 0) {
    body.push("EXIT LOGIC (LONG)");
    for (const r of strategy.exit_rules) body.push(`  - ${ruleText(r)}`);
    body.push("");
  }

  // Short rules
  const shortEntry = strategy.entry_rules_short ?? [];
  const shortExit = strategy.exit_rules_short ?? [];
  if (shortEntry.length > 0) {
    body.push("ENTRY LOGIC (SHORT)");
    for (const r of shortEntry) body.push(`  - ${ruleText(r)}`);
    body.push("");
  }
  if (shortExit.length > 0) {
    body.push("EXIT LOGIC (SHORT)");
    for (const r of shortExit) body.push(`  - ${ruleText(r)}`);
    body.push("");
  }

  // Risk management
  const pm = strategy.position_management;
  body.push("RISK MANAGEMENT");
  const riskPct =
    pm.risk_pct != null ? pm.risk_pct * 100 : (pm.size ?? 0.02) * 100;
  body.push(`  - Risk per trade: ${riskPct.toFixed(1)}%`);
  if (pm.sl_pips != null)       body.push(`  - Fixed SL: ${pm.sl_pips} pips`);
  if (pm.tp_pips != null)       body.push(`  - Fixed TP: ${pm.tp_pips} pips`);
  if (pm.sl_atr_mult != null)   body.push(`  - ATR-based SL: ${pm.sl_atr_mult}\u00d7ATR`);
  if (pm.tp_atr_mult != null)   body.push(`  - ATR-based TP: ${pm.tp_atr_mult}\u00d7ATR`);
  if (pm.trailing_sl)           body.push(`  - Trailing SL: ${pm.trailing_sl}`);
  if (pm.scale_out)             body.push(`  - Scale-out: ${pm.scale_out.volume_pct}% at ${pm.scale_out.trigger_r}R`);
  if (pm.time_exit_bars)        body.push(`  - Time exit: close after ${pm.time_exit_bars} bars in loss`);
  body.push("");
  body.push("WARNING: Review and test thoroughly before live trading");

  if (format === "cs") {
    return [
      "/// <summary>",
      ...body.map((l) => `/// ${l}`.trimEnd()),
      "/// </summary>",
    ].join("\n");
  } else {
    const sep = "// " + "=".repeat(60);
    return [
      sep,
      ...body.map((l) => `// ${l}`.trimEnd()),
      sep,
    ].join("\n");
  }
}
